print("Sucesión similar a la de Fibonacci, empieza sumando el 2 y 1")
n=int(input("Ingrese el n-ésimo término de la sucesión:"))
def lucas(n):
  f0=2
  f1=1
  for i in range(n):
    f_nuevo=f0+f1
    f0=f1
    f1=f_nuevo+f0-f1
  return(f0)
print("Resultado:",lucas(n))

