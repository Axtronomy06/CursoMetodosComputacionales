print("La siguiente función pide un número natural que devuelve su factorial doble")
n=int(input("Ingrese el número:"))
def doble_factorial(n):
  f=1
  for i in range (n,0,-2):
      f=f*i
  return(f)
print("Resultado:",doble_factorial(n))

